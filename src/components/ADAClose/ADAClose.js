class ADAClose extends Component {



	exitADA(){
		console.log('Closing ADA')
		this.props.dispatch( actions.ada.update({ enabled: false, currentElement: null }))
	}

	render(){
		let classes = ['ada-close']
		if( this.props.ada.enabled ) classes.push('enabled')
		return (
			<div className={classes.join(' ')} onClick={() => this.exitADA()}>
				<div className="bordered">
					<FontAwesomeIcon className="fa-3x" icon={['fas', 'times']} />
					<div className="img">{SVG}</div>
				</div>
			</div>
		)
	}
}



